    id INT AUTO_INCREMENT PRIMARY KEY,
    category VARCHAR(40) NOT NULL
);

CREATE TABLE acad_hostel_name (
    id INT AUTO_INCREMENT PRIMARY KEY,
    hostel_name VARCHAR(40) NOT NULL
);

CREATE TABLE acad_hostel_block (
    id INT AUTO_INCREMENT PRIMARY KEY,
    hostel_block VARCHAR(40) NOT NULL
);

CREATE TABLE acad_course (
    id INT AUTO_INCREMENT PRIMARY KEY,
    course VARCHAR(40) NOT NULL
);

-- Main users table
CREATE TABLE acad_users (
    roll_number VARCHAR(40) NOT NULL PRIMARY KEY,
    full_name VARCHAR(100) NOT NULL,
    preferred_name VARCHAR(100),
    dob DATE NOT NULL,
    gender VARCHAR(40) NOT NULL,
    mother_tongue VARCHAR(40) NOT NULL,
    religion VARCHAR(40) NOT NULL,
    nationality VARCHAR(100) NOT NULL,
    state VARCHAR(40) NOT NULL,
    blood_group VARCHAR(10) NOT NULL,
    category VARCHAR(40) NOT NULL,
    country_code VARCHAR(100) NOT NULL,
    mobile_no VARCHAR(40) NOT NULL,
    whatsapp_no VARCHAR(40),
    course VARCHAR(40) NOT NULL,
    department VARCHAR(40) NOT NULL,
    branch VARCHAR(40) NOT NULL,
    specialization VARCHAR(40),
    hostel_name  VARCHAR(40) NOT NULL,
    hostel_block VARCHAR(40) NOT NULL,
    hostel_room_no VARCHAR(40) NOT NULL,
    webmail VARCHAR(100) NOT NULL,
    alt_mail VARCHAR(100),
    date_of_admission DATE NOT NULL,
    father_name VARCHAR(100) NOT NULL,
    mother_name VARCHAR(100) NOT NULL,
    father_phone VARCHAR(20) NOT NULL,
    mother_phone VARCHAR(20) NOT NULL,
    father_profession VARCHAR(40) NOT NULL,
    mother_profession VARCHAR(40) NOT NULL,
    annual_income VARCHAR(20) NOT NULL,
    photo_path VARCHAR(255) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE users (
    roll VARCHAR(20) PRIMARY KEY,
    webmail VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    is_fac TINYINT(1) DEFAULT 0,
    is_head TINYINT(1) DEFAULT 0,
    is_adean TINYINT(1) DEFAULT 0,
    is_dean TINYINT(1) DEFAULT 0,
    is_pic TINYINT(1) DEFAULT 0
);

-- Table for logging queries
CREATE TABLE acad_queries_log (
    id INT AUTO_INCREMENT PRIMARY KEY,
    query TEXT NOT NULL,
    traceback TEXT NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

